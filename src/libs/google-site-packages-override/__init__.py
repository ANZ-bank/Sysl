# This file is used by appvyor.yml to create a Windows exe
# Py2exe does not work with namespace packages (https://goo.gl/BNFt3a)
# placing this file in <site-packages>/google is a workaround (https://goo.gl/c2dTQB)
try:
    import pkg_resources
    pkg_resources.declare_namespace(__name__)
except ImportError:
    import pkgutil
    __path__ = pkgutil.extend_path(__path__, __name__)
